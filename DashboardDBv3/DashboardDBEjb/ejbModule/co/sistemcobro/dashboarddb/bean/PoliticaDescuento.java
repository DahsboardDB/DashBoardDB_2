package co.sistemcobro.dashboarddb.bean;

public class PoliticaDescuento {
	
	private Integer idCargue;
	private String rangoUIT;
	private String rangoMora;
	private Float descMin;
	private Float descMax;
	
	public Integer getIdCargue() {
		return idCargue;
	}
	public void setIdCargue(Integer idCargue) {
		this.idCargue = idCargue;
	}
	public String getRangoUIT() {
		return rangoUIT;
	}
	public void setRangoUIT(String rangoUIT) {
		this.rangoUIT = rangoUIT;
	}
	public String getRangoMora() {
		return rangoMora;
	}
	public void setRangoMora(String rangoMora) {
		this.rangoMora = rangoMora;
	}
	public Float getDescMin() {
		return descMin;
	}
	public void setDescMin(Float descMin) {
		this.descMin = descMin;
	}
	public Float getDescMax() {
		return descMax;
	}
	public void setDescMax(Float descMax) {
		this.descMax = descMax;
	}
	
	

}
